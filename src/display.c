void display_init(void) {
    hal_spi_set_speed_fast();
    tft_reset();

    // Basic ST7735 init sequence (common for 128x160)
    tft_write_cmd(0x01); // SWRESET
    hal_delay_ms(150);

    tft_write_cmd(0x11); // SLPOUT
    hal_delay_ms(150);

    tft_write_cmd(0x3A); // COLMOD: 16-bit color
    tft_write_data(0x05);

    tft_write_cmd(0x36); // MADCTL
    tft_write_data(0x00);

    tft_write_cmd(0x29); // DISPON
    hal_delay_ms(50);
}

void display_fill_color(uint16_t color) {
    display_set_addr_window(128, 160);

    hal_tft_dc_high();
    hal_tft_cs_low();
    uint16_t pixels = (uint16_t)(128u * 160u);
    uint8_t hi = (uint8_t)(color >> 8);
    uint8_t lo = (uint8_t)(color & 0xFF);
    uint8_t buf[1024];
    for (uint16_t i = 0; i < sizeof(buf); i += 2) {
        buf[i] = hi;
        buf[i + 1] = lo;
    }
    uint16_t bytes = (uint16_t)(pixels * 2u);
    while (bytes) {
        uint16_t chunk = bytes > sizeof(buf) ? (uint16_t)sizeof(buf) : bytes;
        hal_spi_write_buffer(buf, chunk);
        bytes = (uint16_t)(bytes - chunk);
    }
    hal_tft_cs_high();
}

void display_set_addr_window(uint16_t width, uint16_t height) {
    tft_set_addr_window(0, 0, width - 1, height - 1);
}

void display_stream_bytes(const uint8_t *data, uint16_t len) {
    hal_tft_dc_high();
    hal_tft_cs_low();
    hal_spi_write_buffer(data, len);
    hal_tft_cs_high();
}

void display_draw_char(uint16_t x, uint16_t y, char c, uint16_t fg, uint16_t bg) {
    if (c < 32 || c > 127) {
        c = '?';
    }
    uint8_t idx = (uint8_t)(c - 32);
    uint8_t buf[6 * 8 * 2];
    uint16_t bi = 0;
    for (uint8_t row = 0; row < 8; row++) {
        for (uint8_t col = 0; col < 6; col++) {
            uint16_t color = bg;
            if (col < 5 && row < 7) {
                uint8_t bits = font5x7[idx][col];
                if (bits & (1u << row)) {
                    color = fg;
                }
            }
            buf[bi++] = (uint8_t)(color >> 8);
            buf[bi++] = (uint8_t)(color & 0xFF);
        }
    }
    tft_set_addr_window(x, y, (uint16_t)(x + 5u), (uint16_t)(y + 7u));
    hal_tft_dc_high();
    hal_tft_cs_low();
    hal_spi_write_buffer(buf, sizeof(buf));
    hal_tft_cs_high();
}

void display_draw_text(uint16_t x, uint16_t y, const char *text, uint16_t fg, uint16_t bg) {
    if (!text) {
        return;
    }
    uint16_t cx = x;
    uint16_t cy = y;
    while (*text) {
        if (*text == '\n') {
            cx = x;
            cy = (uint16_t)(cy + 8u);
            text++;
            continue;
        }
        display_draw_char(cx, cy, *text, fg, bg);
        cx = (uint16_t)(cx + 6u);
        text++;
    }
}
